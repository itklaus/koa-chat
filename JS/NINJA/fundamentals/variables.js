// Variable naming
// There are two limitations for a name in ES:
// 1. The name must contain only letters, digits, symbols $ and _.
// 2. The first character must not be a digit.

// let

let hello = 'Hello, world!';
let message;

message = hello;

// const
// They cannot be changed.

const $_ = 10;


// Some good-to-follow rules are:
// - Use human-readable names like userName or shoppingCart
// - Stay away from abbreviations or shot names like a, b, c, unless you really know what you're doing.
// - Make the name maximally descriptive and concise. Example of bad names are data and value. Such a name says nothing. It's only ok to use them if it's exceptionally obvious from the context which data or value is meant.
